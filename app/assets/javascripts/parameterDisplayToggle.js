function parameterDisplayToggle(ele, dependent, dependents){
  var thd = $('*[class^="toggle_hidden_'+dependent+'_"]');
  for(i = 0; i < thd.length; i++){
    var className = thd[i].className;
    if(className.split("_")[3] == ele.value){
      $("."+className)[0].getElementsByTagName("label")[0].innerHTML = '<abbr title="required">*</abbr>';
      $("."+className).show();
      if($("."+className)[0].getElementsByClassName("file").length != 0){ // due to additional div is generated by simple form for non file inputs
        $("."+className).children().children()[1].required = true;
      }
      else{
        $("."+className).children().children()[1].children[0].required = true;
      }
      //$("."+className).closest(".form-group").find("label")
    }
    else {
      $("."+className).hide();
      if($("."+className)[0].getElementsByClassName("file").length != 0){
        $("."+className).children().children()[1].required = false;
      }
      else{
        $("."+className).children().children()[1].children[0].required = false;
      }
    }
  }
}
